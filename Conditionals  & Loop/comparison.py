# 6 types of comparism
# '<' check small or not
print(12<35)
print(42<35)
# '>' check bigger or not
print(34>13)
print(45>89)
# '>=' equal or bigger
print(12>=12)
# '<=' equal or smaller
print(12<=12)
print(9<=10)
# '==' equal
myMoney = 73;
yourMoney =34;
print(myMoney==yourMoney)
# '!=' not equal
print(myMoney!=yourMoney)